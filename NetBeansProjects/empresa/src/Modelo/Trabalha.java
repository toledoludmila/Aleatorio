package Modelo;

/**
 *
 * @author ludmila
 */
public class Trabalha {
    private int essn;
    private int pno;
    private float hours;
    private int id;

    public int getEssn() {
        return essn;
    }

    public void setEssn(int essn) {
        this.essn = essn;
    }

    public int getPno() {
        return pno;
    }

    public void setPno(int pno) {
        this.pno = pno;
    }

    public float getHours() {
        return hours;
    }

    public void setHours(float hours) {
        this.hours = hours;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
}
